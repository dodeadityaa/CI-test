            <li>Bagi koperasi yang masa berlaku <b>sertifikat NIK telah memasuki masa akhir</b>, dimohon segera melaporkan Berita Acara RAT dan memperbaharui data sesuai formulir <a href="/file/Form Profil Koperasi - Sertifikat NIK.doc"> DOC/</a><a href="/file/Form Profil Koperasi - Sertifikat NIK.pdf">PDF </a> (offline) dan melaporkan ke Dinas yang membidangi Koperasi dan UMKM setempat/sesuai wilayah keanggotaan koperasi untuk cetak perpanjangan masa berlaku sertifikat NIK atau dapat melaporkan melalui website <a href="/odsonline"><b>nik.depkop.go.id</b></a> secara mandiri (<i>sedang dalam pemeliharaan sistem</i>).</li>

