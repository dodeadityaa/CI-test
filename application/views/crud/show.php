            <li><b><a href="/file/Grading Sertifikat NIK.pdf" target="_new">Grade B</a></b>: Koperasi telah melaporkan hasil RAT minimal 2 kali Tahun Buku dalam 3 Tahun Terakhir.</li>
            <li><b><a href="/file/Grading Sertifikat NIK.pdf" target="_new">Grade C1</a></b>: Koperasi yang baru berdiri dalam 3 Tahun terakhir dan melaporkan 1 kali RAT dalam 3 tahun terakhir.</li>
            <li><b><a href="/file/Grading Sertifikat NIK.pdf" target="_new">Grade C2</a></b>:: Koperasi yang berdiri lebih dari 3 tahun, namun baru melaporkan 1 kali RAT pada tahun berjalan.</li>
            <li><b><a href="/file/Grading Sertifikat NIK.pdf" target="_new">Grade D</a></b>: Koperasi belum pernah melaporkan RAT dalam 3 Tahun Buku terakhir. <br>Jika Status NIK "<b>Bersertifikat NIK</b>" namun status Grade "<b>D</b>" = masa berlaku "<b>Sertifikat NIK telah habis</b>".</li>
